/**********************************************
 * Function to check if n = p^a for some a >= 0
 * ********************************************/
IsPrimePower := function(n, p)
	assert IsPrime(p);
	while n gt 1 do
		if IsDivisibleBy(n, p) eq true then
			n := n div p;
		else
			return false;
		end if;
	end while;
	return true;
end function;

